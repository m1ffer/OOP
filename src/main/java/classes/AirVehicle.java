package classes;

import java.util.Objects;

/**
 * Абстрактный класс воздушного транспортного средства.
 *
 * <p>Расширяет {@link Vehicle} и добавляет модель ограничения высоты полёта.
 * Объект может перемещаться только в пределах заданного диапазона координаты Y.</p>
 *
 * <h3>Особенности модели:</h3>
 * <ul>
 *     <li>Высота интерпретируется как координата Y в системе JavaFX.</li>
 *     <li>Ось Y направлена вниз: меньшее значение — выше, большее — ниже.</li>
 *     <li>Полёт ограничен диапазоном {@code [upperBound, lowerBound]}.</li>
 * </ul>
 *
 * <h3>Гарантии класса:</h3>
 * <ul>
 *     <li>При создании объекта проверяется, что его начальная позиция и габариты
 *     полностью помещаются в допустимый диапазон высоты.</li>
 *     <li>Во время движения координата Y автоматически ограничивается
 *     (clamping) в пределах допустимого диапазона.</li>
 * </ul>
 *
 * <p>Класс не реализует конкретную модель полёта (синусоида, линейное движение,
 * отскок и т.п.), а лишь обеспечивает геометрические ограничения высоты.
 * Конкретная логика движения задаётся через функции {@code yMotion}.</p>
 */
public abstract class AirVehicle extends Vehicle{

    /**
     * Нижняя граница допустимой высоты полёта (большее значение Y).
     *
     * <p>В системе координат JavaFX ось Y направлена вниз,
     * поэтому большее значение означает более низкое положение
     * объекта на канве.</p>
     *
     * <p>Объект не может опускаться ниже данной координаты.</p>
     */
    protected final double lowerBound;

    /**
     * Верхняя граница допустимой высоты полёта (меньшее значение Y).
     *
     * <p>В системе координат JavaFX ось Y направлена вниз,
     * поэтому меньшее значение означает более высокое положение
     * объекта на канве.</p>
     *
     * <p>Объект не может подниматься выше данной координаты.</p>
     */
    protected final double upperBound;

    /**
     * Создаёт воздушное транспортное средство с заданным диапазоном высоты полёта.
     *
     * <p>Конструктор:</p>
     * <ul>
     *     <li>Инициализирует базовую транспортную логику через {@link Vehicle}.</li>
     *     <li>Сохраняет допустимые границы высоты полёта.</li>
     *     <li>Проверяет, что начальная позиция объекта и его габариты
     *     полностью помещаются в заданный диапазон высоты.</li>
     * </ul>
     *
     * <p>Высота интерпретируется как координата Y на канве JavaFX:
     * меньшее значение — выше, большее — ниже.</p>
     *
     * @param imageConf      конфигурация отображения объекта (не null)
     * @param motionConf     конфигурация функций движения (не null)
     * @param loadConf       конфигурация загрузки транспортного средства (не null)
     * @param reductionConf  конфигурация уменьшения скорости (не null)
     * @param flightConf     конфигурация допустимого диапазона высоты полёта (не null)
     *
     * @throws NullPointerException если {@code flightConf} равен null
     * @throws IllegalArgumentException если:
     * <ul>
     *     <li>начальная высота объекта выше допустимой верхней границы</li>
     *     <li>нижняя часть объекта выходит за допустимую нижнюю границу</li>
     * </ul>
     */
    protected AirVehicle(ImageConf imageConf,
                         MotionConf motionConf,
                         LoadConf loadConf,
                         ReductionConf reductionConf,
                         FlightConf flightConf
    ) {
        super(
                imageConf,
                motionConf,
                loadConf,
                reductionConf
        );

        lowerBound = Objects.requireNonNull(flightConf,
                "Конфигурация допустимого диапазона высоты полёта не должна быть null").lowerBound();
        upperBound = flightConf.upperBound();

        double startY = imageConf.startY();
        double objectHeight = imageConf.height();

        // Проверка, что верх объекта не выше допустимой верхней границы
        validateMin(startY, upperBound,
                "Начальная высота объекта выходит выше допустимой верхней границы полёта.");

        // Проверка, что нижняя часть объекта не выходит ниже допустимой нижней границы
        validateMax(startY + objectHeight, lowerBound,
                "Габариты объекта выходят за допустимую нижнюю границу полёта.");
    }

    /**
     * Обновляет положение воздушного транспортного средства.
     *
     * <p>Метод выполняет следующие действия:</p>
     * <ul>
     *     <li>Масштабирует внутреннее время жизни объекта с учётом
     *     коэффициента уменьшения скорости (reduction).</li>
     *     <li>Пересчитывает координату {@code x} через функцию движения по оси X.</li>
     *     <li>Пересчитывает координату {@code y} через функцию движения по оси Y
     *     с последующим ограничением в пределах допустимого диапазона высоты
     *     {@code [upperBound, lowerBound]}.</li>
     * </ul>
     *
     * <p>Ограничение высоты выполняется по принципу "жёсткой границы"
     * (clamping): если вычисленное значение выходит за пределы диапазона,
     * оно принудительно приводится к ближайшей границе.</p>
     *
     * @param deltaSeconds количество секунд, прошедших с предыдущего обновления
     */
    @Override
    public void move(double deltaSeconds){
        // Увеличиваем время жизни с учётом коэффициента скорости
        elapsedTime += deltaSeconds * (1 - getReduction());

        // Пересчитываем координаты через функции движения
        x = xMotion.apply(elapsedTime);
        y = Math.min(Math.max(
                yMotion.apply(elapsedTime),
                upperBound),
                lowerBound);
    }

    // =======================
    // ===== КОНФИГ ==========
    // =======================

    /**
     * Конфигурация допустимого диапазона высоты полёта
     * для воздушного транспорта.
     *
     * <p>Высота задаётся в координатах канвы (ось Y):
     * <ul>
     *     <li>0 — верх канвы</li>
     *     <li>Чем больше значение, тем ниже объект</li>
     * </ul>
     *
     * <p>Нижняя граница должна быть больше верхней,
     * так как находится ниже на канве.</p>
     */
    public record FlightConf(
            double lowerBound, // нижняя граница (большее значение Y)
            double upperBound  // верхняя граница (меньшее значение Y)
    ) {

        /**
         * Canonical-конструктор с проверками корректности диапазона.
         *
         * @throws IllegalArgumentException если:
         * <ul>
         *     <li>любая из границ отрицательна</li>
         *     <li>нижняя граница не больше верхней</li>
         * </ul>
         */
        public FlightConf {
            // Проверка неотрицательности
            validateNonNegative(lowerBound,
                    "Нижняя граница высоты не может быть отрицательной.");
            validateNonNegative(upperBound,
                    "Верхняя граница высоты не может быть отрицательной.");

            // Проверка корректности диапазона
            validateMinEquals(lowerBound, upperBound,
                    "Нижняя граница высоты должна быть строго больше верхней "
                            + "(в координатах канвы Y растёт вниз).");
        }
    }
}
